package edu.sidnei;

public class TiposDeLoops {
    public static void main(String[] args){
        //loop clássico
        for(int carneirinhos = 1 //tipo
            ; carneirinhos <=20; //condição
            carneirinhos++) // atualização
        {
            //execução
            System.out.println(carneirinhos);
        }
    }
}
